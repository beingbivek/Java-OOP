// package Week1.Helloworld;

public class PatternPrint {
    public static void main(String[] args){
        System.out.println('*');
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("*****");
        System.out.println("\nAnother Pattern\n");
        System.out.println("*********");
        System.out.println("*********");
        System.out.println("*********");
        System.out.println("*********");
        System.out.println("*********");
        System.out.println("*********");
        System.out.println("\nAnother Pattern Using For Loop\n");
        for (int i = 0; i < 5; i++) {
            System.out.println("*********");
        }
    }
    
}
